function renderSDTable(rows, currentAge = null) {
    const labels = ["ฐานวัน", "ฐานเดือน", "ฐานปี", "ฐานบวก"];
    const posNames = [
        ["อัตตา", "หินะ", "ธนัง", "ปิตา", "มาตา", "โภคา", "มัชฌิมา"],
        ["ตะนุ", "กดุมพะ", "สหัชชะ", "พันธุ", "ปุตตะ", "อริ", "ปัตตนิ"],
        ["มรณะ", "สุภะ", "กัมมะ", "ลาภะ", "พยายะ", "ทาสี", "ทาสา"]
    ];    
    
    // ตรวจสอบข้อมูลเบื้องต้น
    if (!rows || rows.length < 4) return;
    // คำนวณเสาจร: (อายุ - 1) % 7
    const transitIdx = currentAge ? (parseInt(currentAge) - 1) % 7 : -1;

    let html = `<div class="table-responsive"><table class="table table-bordered text-center text-white border-secondary">`;
    html += `<thead><tr class="bg-dark text-gold"><th>ฐาน / เสา</th>`;
    for(let i=1; i<=7; i++) {
        let headerStyle = (i-1 === transitIdx) ? "style='background-color: #d4af37; color: #000;'" : "";
        html += `<th ${headerStyle}>${i} ${i-1 === transitIdx ? '<i class="fas fa-star"></i>' : ''}</th>`;
    }
    html += `</tr></thead><tbody>`;

    for (let r = 0; r < 4; r++) {
        let rowClass = ["row-early", "row-mid", "row-late", "row-summary"][r];
        html += `<tr class="${rowClass}"><td class="bg-dark text-gold-big font-bold " style="width: 70px; height: 50px;">${labels[r]}</td>`;        
        for (let c = 0; c < 7; c++) {
            let num = rows[r][c];
            let isBase4 = (r === 3);
            let isTransitCol = (c === transitIdx);
            
            let cellStyle = `cursor: pointer; position: relative; transition: 0.3s;`;
            if (isTransitCol) cellStyle += `border: 2px solid #d4af37 !important; background: rgba(212, 175, 55, 0.15);`;
            if (isBase4) cellStyle += `color: #00ff00; font-weight: bold;`;

            let clickAttr = "";
            if (!isBase4) {
                let pName = posNames[r][c];
                // ป้องกัน Error ถ้าไม่มี houseMeanings
                let pDesc = (window.houseMeanings && houseMeanings[pName]) ? houseMeanings[pName] : "ความหมายตามตำแหน่งภพ";
                // ใช้ encodeURIComponent หรือล้างเครื่องหมายคำพูดเพื่อป้องกัน HTML พัง
                clickAttr = `onclick="showCellDetail('${pName.replace(/'/g, "\\'")}', ${num}, '${pDesc.replace(/'/g, "\\'")}', false)"`;
            } else {
                // ป้องกัน Error ถ้าไม่มี base4ColumnMeanings
                let b4Title = (window.base4ColumnMeanings && base4ColumnMeanings[c]) ? base4ColumnMeanings[c] : `<br>ฐานสรุปเสาที่ ${c+1}`;
                clickAttr = `onclick="showCellDetail('${b4Title.replace(/'/g, "\\'")}', ${num}, 'ฐานสรุปกำลังพระเคราะห์', true)"`;
            }

            let subName = (r < 3) ? posNames[r][c].split(' ')[0] : "";
            let transitBadge = (isTransitCol && r === 0) ? `<span class="badge badge-warning" style="position:absolute; top:0; right:0; font-size:0.5rem;">ปีจร</span>` : "";

            html += `
                <td style="${cellStyle}" ${clickAttr} class="sd-cell px-1">
                    ${transitBadge}
                    <div class="h5 mb-0">${num}</div>
                    ${subName ? `<small class="text-white-50" style="font-size: 0.65rem">${subName}</small>` : ""}
                </td>`;
        }
        html += `</tr>`;
    }
    html += `</tbody></table></div>`;
    document.getElementById('sevenDigitsTable').innerHTML = html;
}


