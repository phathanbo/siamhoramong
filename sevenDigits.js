function getThaksa(num, dayNum, age) {
    if (!age) return null;
    let startIdx = thaksaOrder.indexOf(dayNum);
    let currentPosIdx = (startIdx + (age - 1)) % 8;
    let numIdx = thaksaOrder.indexOf(num === 8 ? 8 : num % 10);
    let thaksaIdx = (numIdx - currentPosIdx + 8) % 8;
    return { name: thaksaNames[thaksaIdx] };
}

