function analyzeSevenDigits(bases){
    const result = [];
    const destiny = bases[0];
    const wealth = bases[3];
    const friend = bases[2];
    const enemy = bases[5];
    const love = bases?.[6] || 0;

    if(destiny >= 7){
        result.push("⭐ วาสนาเด่น มีโอกาสก้าวหน้าในชีวิต");
    }else if(destiny >= 4){
        result.push("🌱 ชีวิตค่อย ๆ เติบโต ต้องอาศัยความพยายาม");
    }else{
        result.push("⚠ วาสนาต้องสร้างเอง เหนื่อยก่อนสบายทีหลัง");
    }

    if(wealth >= 7){
        result.push("💰 การเงินดี หาเงินเก่ง");
    }else if(wealth >= 4){
        result.push("💵 รายได้เรื่อย ๆ มีเข้ามา");
    }else{
        result.push("⚠ การเงินขึ้นลง ต้องระวังการใช้จ่าย");
    }

    if(friend >= 6){
        result.push("🤝 มีผู้ใหญ่หรือเพื่อนคอยช่วยเหลือ");
    }

    if(enemy <= 3){
        result.push("⚠ ระวังคนอิจฉาหรือศัตรู");
    }

    if(love >= 6){
        result.push("❤️ ความรักมีความมั่นคง");
    }else{
        result.push("💔 ความรักต้องใช้เวลาเรียนรู้กัน");
    }

    return result;

}


function calculateLuckScore(bases){
    let score = 50; // เริ่มกลาง ๆ
    const destiny = bases[0];
    const wealth = bases[3];
    const friend = bases[2];
    const enemy = bases[5];
    const love = bases[6] || 0;
    // วาสนา
    if(destiny >= 7) score += 15;
    else if(destiny <= 3) score -= 10;
    // การเงิน
    if(wealth >= 7) score += 15;
    else if(wealth <= 3) score -= 10;
    // เพื่อน
    if(friend >= 6) score += 10;
    // ศัตรู
    if(enemy <= 3) score -= 10;
    // ความรัก
    if(love >= 6) score += 5;
    if(score > 100) score = 100;
    if(score < 0) score = 0;
    return score;
}


