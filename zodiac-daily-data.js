/**
 * คำนวณหาภูมิทักษาของดาวประจำราศี ในวันนั้นๆ
 * @param {number} dayOfWeek - 0 (Sun) to 6 (Sat)
 * @param {number} signRuler - ดาวเกษตร (1,2,3,4,5,6,7,8)
 * @returns {number} 0-7 (บริวาร - กาลกิณี)
 */
function getTaksaPosition(dayOfWeek, signRuler) {
    // แปลง dayOfWeek ให้เป็นดาวประจำวันตามทักษา
    // 0=Sun(1), 1=Mon(2), 2=Tue(3), 3=Wed(4), 4=Thu(5), 5=Fri(6), 6=Sat(7)
    // แต่ทักษาพฤหัสคือ 5, เสาร์คือ 7
    let dayPlanet;
    switch(dayOfWeek) {
        case 0: dayPlanet = 1; break;
        case 1: dayPlanet = 2; break;
        case 2: dayPlanet = 3; break;
        case 3: dayPlanet = 4; break;
        case 4: dayPlanet = 5; break;
        case 5: dayPlanet = 6; break;
        case 6: dayPlanet = 7; break;
        default: dayPlanet = 1;
    }

    const startIdx = TAKSA_ORDER.indexOf(dayPlanet);
    const targetIdx = TAKSA_ORDER.indexOf(signRuler);

    let offset = targetIdx - startIdx;
    if (offset < 0) offset += 8;

    return offset;
}

/**
 * Generate 12 Zodiac predictions for a specific date
 * @param {Date} dateObj - Date to generate for
 * @returns {Array} Array of 12 prediction objects
 */
function generateDailyZodiacFortunes(dateObj) {
    const dayOfWeek = dateObj.getDay(); // 0-6
    
    // ใช้สัปดาห์ของปีเพื่อหมุนเวียน variation (0, 1, 2)
    const firstDayOfYear = new Date(dateObj.getFullYear(), 0, 1);
    const pastDaysOfYear = (dateObj - firstDayOfYear) / 86400000;
    const weekNumber = Math.ceil((pastDaysOfYear + firstDayOfYear.getDay() + 1) / 7);
    const variationIdx = weekNumber % 3;

    let predictions = [];

    for (let i = 1; i <= 12; i++) {
        const sign = ZODIAC_RULERS[i];
        const taksaPos = getTaksaPosition(dayOfWeek, sign.ruler);
        const meaning = TAKSA_MEANINGS[taksaPos];
        const specificMeaning = meaning.variations[variationIdx];

        predictions.push({
            id: sign.id,
            name: sign.name,
            icon: sign.icon,
            dateRange: sign.dateRange,
            taksa: meaning.name,
            description: specificMeaning.desc,
            good: "ดี : " + specificMeaning.good,
            bad: "ควรระวัง : " + specificMeaning.bad
        });
    }

    return predictions;
}
