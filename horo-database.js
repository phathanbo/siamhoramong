// ฟังก์ชันคำนวณสัตตเลขพื้นฐาน (Logic เดิมจาก taksasattalek)
function calculateSattalekBase(day, month, year) {
    let matrix = [[], [], [], []];
    const genBase = (start) => Array.from({length: 7}, (_, i) => ((start - 1 + i) % 7) + 1);
    
    matrix[0] = genBase(day);
    matrix[1] = genBase(month);
    matrix[2] = genBase(year);
    for (let i = 0; i < 7; i++) {
        matrix[3][i] = matrix[0][i] + matrix[1][i] + matrix[2][i];
    }
    return matrix;
}

