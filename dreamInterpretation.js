// --- 1. ส่วนจัดการ Suggestions และ Event Listeners ---
document.addEventListener('DOMContentLoaded', () => {
    const input = document.getElementById('dreamKeyword');
    const suggestionsList = document.getElementById('dreamSuggestions'); 
    
    // ตรวจสอบความพร้อมของ Element
    if (!input || !suggestionsList) {
        console.warn("⚠️ ไม่พบ Element: dreamKeyword หรือ dreamSuggestions ในหน้า HTML");
        return;
    }

    // ดึง Key ทั้งหมดและเรียงลำดับตามตัวอักษรไว้รอเลย (ทำครั้งเดียว)
    const allKeys = Object.keys(dreamDatabase).sort();

    /**
     * ฟังก์ชันสำหรับวาดรายการ Suggestion (Datalist)
     * @param {string} filterValue - ค่าที่ใช้กรองคำ
     */
    const renderSuggestions = (filterValue = "") => {
        const value = filterValue.trim().toLowerCase();
        
        // ใช้ DocumentFragment เพื่อประสิทธิภาพการ Render ที่ดีกว่า (ในกรณีข้อมูล 200+ คำ)
        const fragment = document.createDocumentFragment();
        suggestionsList.innerHTML = '';

        allKeys.forEach(key => {
            const keyLower = key.toLowerCase();
            // เงื่อนไข: ถ้าว่างให้โชว์หมด หรือถ้าพิมพ์ให้เช็ค includes หรือเช็คตัวอักษรประกอบ
            if (value === "" || keyLower.includes(value) || value.split('').every(char => keyLower.includes(char))) {
                const option = document.createElement('option');
                option.value = key;
                fragment.appendChild(option);
            }
        });
        suggestionsList.appendChild(fragment);
    };

    // --- เริ่มต้นการทำงาน ---
    renderSuggestions(); // สร้างรายการครั้งแรกตอนโหลดหน้า

    // Event: เมื่อพิมพ์ในช่องค้นหา (Real-time Filter)
    input.addEventListener('input', () => {
        renderSuggestions(input.value);
    });

    // Event: เมื่อกด Enter
    input.addEventListener('keypress', (e) => {
        if (e.key === 'Enter') {
            interpretDream();
        }
    });

    // Event: เมื่อเลือกจากรายการ Suggestion (Datalist)
    // ใช้รวบกับ input event เพื่อให้เลือกแล้วทำงานทันที หรือใช้ change ตามเดิม
    input.addEventListener('change', () => {
        // เช็คว่าคำที่เลือกอยู่ใน Database จริงๆ ถึงจะรันอัตโนมัติ
        if (allKeys.includes(input.value)) {
            interpretDream();
        }
    });
});

// --- 2. ฟังก์ชันหลักสำหรับการทำนาย ---
function interpretDream() {
    const input = document.getElementById('dreamKeyword');
    if (!input) return;

    const keyword = input.value.trim();
    const resultDiv = document.getElementById('dreamResult');
    const meaningEl = document.getElementById('dreamMeaning');
    const numbersEl = document.getElementById('dreamNumbers');

    if (!resultDiv || !meaningEl || !numbersEl) {
        console.error("⚠️ interpretDream: ไม่พบ Element ที่จำเป็น (dreamResult / dreamMeaning / dreamNumbers)");
        return;
    }

    if (!keyword) {
        alert("🔍 กรุณาพิมพ์หรือเลือกสิ่งที่ฝันเห็นก่อนนะครับ");
        return;
    }

    let found = null;
    let matchedKey = "";

    // ลำดับการค้นหา: 
    // 1. หาคำที่ยาวที่สุดที่ "อยู่ใน" ประโยคที่ผู้ใช้พิมพ์ (เช่น พิมพ์ "ฝันเห็นงูใหญ่" จะเจอ "งูใหญ่" ไม่ใช่แค่ "งู")
    const allKeysSortedByLength = Object.keys(dreamDatabase).sort((a, b) => b.length - a.length);

    for (let key of allKeysSortedByLength) {
        if (keyword.toLowerCase().includes(key.toLowerCase())) {
            found = dreamDatabase[key];
            matchedKey = key;
            break; 
        }
    }

    // แสดงผล
    if (found) {
        // อัปเดตช่อง input ให้ตรงกับคำที่เจอในฐานข้อมูล (เพื่อความชัดเจน)
        input.value = matchedKey;
        
        meaningEl.innerHTML = `<strong>ฝันเห็น ${matchedKey}</strong> <br> ${found.m}`;
        numbersEl.innerHTML = found.n 
            ? `🔢 เลขเด็ด: <span class="text-danger h3" style="font-weight: bold;">${found.n}</span>` 
            : "❌ ไม่มีเลขเด็ดสำหรับคำนี้";
        
        resultDiv.style.display = 'block';
        
        // เลื่อนหน้าจอไปที่ผลลัพธ์
        setTimeout(() => {
            resultDiv.scrollIntoView({ behavior: 'smooth', block: 'center' });
        }, 100);
    } else {
        meaningEl.innerHTML = `😔 ไม่พบคำทำนายสำหรับ "<strong>${keyword}</strong>" <br><small>ลองพิมพ์คำที่ใกล้เคียง เช่น งู, ทอง, ฟันหลุด หรือเลือกจากรายการแนะนำครับ</small>`;
        numbersEl.innerHTML = "";
        resultDiv.style.display = 'block';
    }
}



async function downloadDreamImage(element) {
    // พื้นที่ที่แสดงผลทำนายฝัน (ใน HTML ของคุณคือส่วนที่มีผลลัพธ์)
    const area = document.getElementById('dreamResult'); 
    if (!area || area.style.display === 'none') {
        alert("กรุณาค้นหาความฝันก่อนทำการเซฟภาพครับ");
        return;
    }

    let btn = element instanceof HTMLElement ? element : document.querySelector('.share-dream-btn');
    const originalText = btn ? btn.innerHTML : "";

    if (btn) {
        btn.innerHTML = '<i class="fas fa-spinner fa-spin"></i> กำลังเตรียมเลขเด็ด...';
        btn.disabled = true;
    }

    // สร้าง Footer สำหรับแบรนด์
    const footer = document.createElement('div');
    footer.innerHTML = `
        <div style="display: flex; justify-content: center; align-items: center; margin-top: 25px; padding-top: 15px; border-top: 1px solid rgba(212,175,55,0.3); color: #d4af37;">            
            <div style="font-size: 12px; opacity: 0.8;">เลขเด็ดแม่นๆ จากตำราทำนายฝัน</div>
        </div>
    `;

    // เก็บ style เดิมและ flag ว่า append footer ไปแล้วหรือยัง
    const originalStyle = area.style.cssText;
    let footerAppended = false;

    try {
        // ปรับแต่งให้ภาพออกมาเป็นจัตุรัสสวยๆ
        area.style.width = "500px";
        area.style.padding = "40px";
        area.style.background = "#1a1a1a";
        area.style.borderRadius = "0px"; // ถ่ายรูปแล้วขอบเหลี่ยมจะดูเต็มกว่า

        area.appendChild(footer);
        footerAppended = true;

        const options = {
            backgroundColor: '#6b6868',
            scale: 2.5, // ชัดพิเศษสำหรับตัวเลข
            useCORS: true,
            allowTaint: false,
            scrollY: -window.scrollY,
            windowWidth: 500
        };

        const canvas = await html2canvas(area, options);

        // แปลง toBlob เป็น Promise เพื่อให้ await ได้ถูกต้อง
        // (ป้องกัน finally รันก่อน download เสร็จ)
        const blob = await new Promise(resolve => canvas.toBlob(resolve, 'image/png', 0.9));

        if (blob) {
            const url = URL.createObjectURL(blob);
            const link = document.createElement('a');
            const dreamWord = document.querySelector('#dreamResult strong')?.innerText || 'ทำนายฝัน';

            link.download = `เลขเด็ด_${dreamWord.replace(/[:\s]+/g, '_')}.png`;
            link.href = url;
            link.click();

            setTimeout(() => URL.revokeObjectURL(url), 100);
        }

    } catch (e) {
        console.error("Dream Share Error:", e);
        alert("ไม่สามารถสร้างภาพได้: " + e.message);
    } finally {
        // คืนค่าเดิมเสมอ ไม่ว่าจะสำเร็จหรือ error
        if (footerAppended && area.contains(footer)) {
            area.removeChild(footer);
        }
        area.style.cssText = originalStyle;
        if (btn) {
            btn.innerHTML = originalText;
            btn.disabled = false;
        }
    }
}


