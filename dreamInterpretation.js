/**
 * 💡 ระบบแนะนำคำค้นหา (Autocomplete)
 */
function showDreamSuggestions(inputText) {
    const suggestionsBox = document.getElementById('dreamSuggestions');
    inputText = inputText.trim().toLowerCase();
    
    if (inputText.length < 1) {
        suggestionsBox.style.display = 'none';
        return;
    }

    // รวบรวมคำศัพท์ทั้งหมดที่มีในฐานข้อมูล
    let allKeywords = [];
    for (let category in DREAM_MEANINGS) {
        for (let keyword in DREAM_MEANINGS[category]) {
            allKeywords.push(keyword);
        }
    }

    // กรองคำที่ตรงกับที่พิมพ์
    let matches = allKeywords.filter(kw => kw.includes(inputText));
    
    // เรียงให้คำที่ขึ้นต้นด้วยข้อความที่พิมพ์อยู่ก่อน
    matches.sort((a, b) => {
        let aStarts = a.startsWith(inputText);
        let bStarts = b.startsWith(inputText);
        if (aStarts && !bStarts) return -1;
        if (!aStarts && bStarts) return 1;
        return a.length - b.length; // สั้นกว่าขึ้นก่อน
    });

    // จำกัดแค่ 10 คำเพื่อไม่ให้รก
    matches = matches.slice(0, 10);

    if (matches.length > 0) {
        let html = matches.map(kw => `
            <button type="button" class="list-group-item list-group-item-action text-white" 
                    style="background: rgba(20, 30, 55, 0.95); border-left: 3px solid #d4af37; border-bottom: 1px solid rgba(212, 175, 55, 0.2);"
                    onclick="setDreamKeyword('${kw}')">
                <i class="fas fa-search text-gold mr-2" style="font-size: 0.8rem;"></i> ${kw}
            </button>
        `).join('');
        suggestionsBox.innerHTML = html;
        suggestionsBox.style.display = 'block';
    } else {
        suggestionsBox.style.display = 'none';
    }
}

// ปิดกล่องคำแนะนำเมื่อคลิกที่อื่น
if (typeof document !== 'undefined') {
    document.addEventListener('click', function(e) {
        const suggestionsBox = document.getElementById('dreamSuggestions');
        const dreamDesc = document.getElementById('dreamDesc');
        if (suggestionsBox && dreamDesc && e.target !== dreamDesc && !suggestionsBox.contains(e.target)) {
            suggestionsBox.style.display = 'none';
        }
    });
}

