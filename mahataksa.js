function getSubPeriodDays(mainPlanetYears, subPlanetYears) {
    // สูตร: (ปีหลัก * ปีแทรก * 360) / 108
    const totalDays = (mainPlanetYears * subPlanetYears * 360) / 108;
    return Math.floor(totalDays);
}

